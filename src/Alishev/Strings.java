package Alishev;

// Урок 3. Строки(String). Ссылочные типы данных.
public class Strings {
    public static void main(String[] args) {
        int x = 5;
        String s = "Hello";
        String space = " ";
        String name = "Bob";
        System.out.println(s + space + name);
        System.out.println("Hello" + " " + "John");
        System.out.println("My number is" + x);
    }
}
