package Alishev;
// Урок 6. Условный оператор if
public class If {
    public static void main(String[] args) {
        int myInt = 5;
        if (myInt < 10) {
            System.out.println("да, верно.");
        } else if (myInt < 20) {
            System.out.println("нет, неверно");
        }
    }
}
