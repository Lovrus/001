package Java_H_Schildt.chapter_1_5;

// Глава по использованию запятой
public class Sample {
    public static void main(String[] args) {
        int a, b;

        b = 4;
        for (a = 1; a < b; a++) {
            System.out.println("a = " + a);
            System.out.println("b = " + b);
            b--;
        }
    }
}
